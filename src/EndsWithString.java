public class EndsWithString {
    public static void main(String[] args) {
        String a = "Fadillah";

        System.out.println("Hasilnya:"+a.endsWith("F"));
        System.out.println("Hasilnya:"+a.endsWith("a"));
        System.out.println("Hasilnya:"+a.endsWith("dil"));
        System.out.println("Hasilnya:"+a.endsWith("lah"));

    }
}